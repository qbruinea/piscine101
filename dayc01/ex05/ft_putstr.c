#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int cmpt;

	cmpt = 0;
	while (str[cmpt] != '\0')
	{
		ft_putchar(str[cmpt]);
		cmpt++;
	}
}

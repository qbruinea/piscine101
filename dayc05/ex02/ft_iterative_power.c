int		ft_iterative_power(int nb, int power)
{
	int i;
	int nbr;

	nbr = 1;
	i = 0;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	while (i < power)
	{
		nbr = nbr * nb;
		i++;
	}
	return (nbr);
}
